class StringConcatination{
    public static void main(String args[]){
        String s1 = "java";
        String s2 = "programming";
        System.out.println(s1+s2);
        System.out.println(s1.concat(s2));
        System.out.println(s2.concat(s1));
        System.out.println(10+s1+s2+23);
    }
}