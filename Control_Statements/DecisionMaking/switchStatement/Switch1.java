public class Switch1{
    public static void main (String[] args){
        int num=2;
        switch(num){
            case 0:
            System.out.println("The number is 0");
            break;
            case 1:
            System.out.println("The number is 1");
            break;
            default:
            System.out.println("The given number is " + num);
        }

    }
}